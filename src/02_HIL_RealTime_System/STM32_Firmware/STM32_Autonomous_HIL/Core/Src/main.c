void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    if (htim->Instance == TIM6)
    {

        // --- 1. GÜVENLİK KONTROLÜ (WATCHDOG) ---
        heartbeat_counter++;
        if (heartbeat_counter > 50)
        {
        	// 500ms boyunca yeni veri gelmediyse
            is_connected = 0;
            // Acil Durum: Hızı kes, direksiyonu düzelt!
            Control_Command_t stop_cmd = {0xBB, 0.0f, -1.0f, 0x00};
            HAL_UART_Transmit(&huart2, (uint8_t*)&stop_cmd, sizeof(Control_Command_t), 10);
            HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, GPIO_PIN_SET); // Kırmızı LED Sabit Yanar
        }

        // --- 2. OTONOM MANTIK ÇALIŞTIR ---
        if (is_connected)
        {
            // Ham veriyi analiz et
            Perception_Process(&perc_state, &raw_rx_packet);

            // Karar ver ve komut üret
            Logic_Update(&auto_brain, &perc_state, &steer_pid);

            // Komutu Python'a (CARLA) geri gönder
            HAL_UART_Transmit(&huart2, (uint8_t*)&auto_brain.last_command, sizeof(Control_Command_t), 10);

            // Mavi LED yan-sön (Sistem çalışıyor "Heartbeat")
            HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_15);
        }
    }
}

// UART'tan her veri geldiğinde bu fonksiyon tetiklenir
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == USART2)
    {
        // Gelen verinin checksum'ını doğrula
        if (HIL_Validate_Checksum(&raw_rx_packet))
        {
            is_connected = 1;
            heartbeat_counter = 0; // Sayacı sıfırla, bağlantı canlı!
        }
    }
}

